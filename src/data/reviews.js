// Helper: generate a consistent color from a string (for avatars)
function stringToColor(str) {
  const colors = [
    ['#006233', '#004d27'],
    ['#D21034', '#a8001f'],
    ['#C9A96E', '#a8834a'],
    ['#1a6b8a', '#0d4d63'],
    ['#7c3aed', '#5b21b6'],
    ['#d97706', '#b45309'],
    ['#0891b2', '#0e7490'],
    ['#be123c', '#9f1239'],
  ]
  let hash = 0
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash)
  }
  return colors[Math.abs(hash) % colors.length]
}

